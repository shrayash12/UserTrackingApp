package shradha.com.finalloginsignupauthproj;

public class User {
    public String userName;
    public String age;
    public String email;
    public int uId;

    public User() {

    }

    public User(String userName, String age, String email, int uId) {
        this.userName = userName;
        this.age = age;
        this.email = email;
        this.uId = uId;
    }

}
